ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	MESSAGE DB "THESE ARE THE STRINGS$" ;$ delimiter is very important
DATA ENDS
CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AH,9		; DOS FUNCTION #9
	LEA DX, MESSAGE		; OFFSET OF THE STRING
	INT 21H			; DISPLAY IT
	
	MOV Ah,4CH
	INT 21H
	CODE ENDS
END START